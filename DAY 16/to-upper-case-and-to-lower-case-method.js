//1).toUpperCase = whith the help of "toUpperCase" we can change the all letter in Uppercase of the string . (its also return a new string its don't change the value of old string ) 
// Examle = 
let personName = "shubham";
let newName = personName.toUpperCase();
console.log(newName);

// we get output - SHUBHAM (in capital letter)

