// Question = Print all even numbers (2 to 20)
for (i = 2; i<=20; i+=2) {
    console.log(i);
}

// We can also print this number in backward way
console.log("In backword way to print even number between 20 to 2")
for (i = 20; i>=2; i-=2) {
    console.log(i);
}