// We have two types of unary operators
// 1. Pre-Increment (ChannelMergerNode, then use)
// 2. Post-Increment (Use, then change)

// 1. Pre-Increment (ChannelMergerNode, then use)
// = in the pre-incremant firstly change the value of variable then it variable use in an expression
// Example = 

// let age = 10;
// let newAge = ++age; // here firstely value of age is change (update) then use age value.

// if we print value of age = ?
// age
// output = 11

// if we print value of newAge = ?
// newAge
// Output = 11


// 2. Post-Increment (Use, then change)

// Example 1:
// let age = 10;
// let newAge = age++; // here firstely value of age is used(for newAge) then change (update value the variable(age)) 

// If we print value of age = ?
// age
// Output = 11

// If we print value of newAge = ?
// newAge
// Output = 10

// Example 2:
// let a = 6;
// let b = a++; // Here firstely used old variable to store in "b" os, Here store value of a = 6 in "variable b"
// then "a variable is update" (a++ = 7)
// if we print value of a = ?
// a
// Output = 7

// if we print value of b = ?
// b
// Output = 6


