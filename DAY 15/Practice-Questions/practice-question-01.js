// Traffic Light System

let color = "green"; // here we can store color red,yellow,green.
if (color === "red"){
    console.log("Stop!, traffic light is red.");
}else if (color === "yellow") {
    console.log("Shlow Down, traffic light is yellow.");
}else if (color === "green") {
    console.log("Go now!, trafffic light is green.");
}

